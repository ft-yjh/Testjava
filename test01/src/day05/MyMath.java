package day05;

public class MyMath {
    public static void main(String[] args) {
        //Math内有许多静态方法
        //abs绝对值 ceil向下取整 floor向上取整 round四舍五入
        System.out.println(Math.abs(-1));
        System.out.println(Math.ceil(3.1));
        System.out.println(Math.ceil(3.8));
        System.out.println(Math.floor(4.1));
        System.out.println(Math.floor(4.8));
        System.out.println(Math.round(5.1));
        System.out.println(Math.round(5.8));
    }
}
